#include <iostream>

using namespace std;

int main() // First things C++ will look for is this main() function.
{
    cout << "   /|" << endl; // cout means console output the string.
    cout << "  / |" << endl; // endl means end of the line of string.
    cout << " /  |" << endl;
    cout << "/___|" << endl;
    cout << endl; // This will create a blank line.

    return 0;
    
}